/**
 * 按标签过滤方法
 * @param {string} tag - 'all' | 'causal' | 'ml' | 'econometric' | 'qual' | 'theory' | 'text' | 'comp'
 */
export function filterMethods(tag = 'all') {
  if (tag === 'all') return METHODS;
  return METHODS.filter(m => m.tags.includes(tag));
}
